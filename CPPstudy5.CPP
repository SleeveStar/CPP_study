#include <iostream>

using namespace std;

int main(){

    /*
    + : 두 개의 피연산자의 합을 계산한다.
    - : 첫 번째의 피연산자에서 두 번째의 피연산자의 차를 계산한다.
    * : 두 개의 피연산자의 곱을 계산한다.
    / : 첫 번째 피연산자에서 두 번째 피연산자를 나눈다.
    >> 두개의 피연산자가 모두 정수이면 결과값은 '몫'이다.
    % : 첫 번째 피연산자를 두 번째 피연산자로 나누어 나머지를 구한다.
    예를 들어서 19를 6으로 나누면 그 값은 1이다.
    부동 소수점(실수) 사용할 수 없다.
    */

    //auto라는 데이터형은 자동으로 변수의 데이터형을 정한다.


    int a = 10;
    int b = 3;

    int c = a + b;
    int d = a - b;
    int e = a * b;
    int f = a / b;
    int g = a % b;

    //c++ 가 복잡한 산술 연산을 수행할 수 있을까? > 가능하지만 연산규칙을 알아야한다. (일반적인 대수학의 규칙을 따른다.)
    //동일 우선순위의 곱셈과 나눗셈같은 경우 우선순위는 왼쪽에서 오른쪽 진행방향으로 우선순위가 정해진다.

    cout << c << endl;
    cout << d << endl;
    cout << e << endl;
    cout << f << endl;
    cout << g << endl;


    return 0;
}